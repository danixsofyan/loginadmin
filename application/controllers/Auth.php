                'alamat'            => "",
                'jmlhfounder'       => "",
                'jmlhpersonil'      => "",
                'email'             => "",
                'facebookstartup'   => "",
                'twitterstartup'    => "",
                'linkedinstartup'   => "",
                'deskripsi'         => "",
                'created_by'        => "SYSTEM",
                'created_date'      => date("Y-m-d h:i:s"),
                'id_socio'          => $last+1
            ];

